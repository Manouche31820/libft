#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	d;

	d = c;
	i = 0;
	while (((char *)s)[i] != d)
	{
		if (s[i] == '\0')
			return (NULL);
		i++;
	}
	return (&((char *)s)[i]);
}
